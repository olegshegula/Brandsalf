package com.bs.selenium.model;

public class Files {
	private String name;
	private String path;
	
	public String getName() {
		return name;
	}

	public Files setName(String name) {
		this.name = name;
		return this;
	}

	public String getPath() {
		return path;
	}

	public Files setPath(String path) {
		this.path = path;
		return this;
	}
	

}
